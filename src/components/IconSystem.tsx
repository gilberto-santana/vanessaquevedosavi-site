// Sistema de importação segura para Lucide React
let lucideReact: any = null;
try {
  // Tentar importar Lucide React de forma segura
  lucideReact = require('lucide-react');
} catch (error) {
  console.warn('Lucide React não disponível, usando ícones essenciais');
}

// Função para obter ícones de forma segura
export const getIcon = (iconName: string) => {
  // Primeiro tentar Lucide React
  if (lucideReact && lucideReact[iconName]) {
    return lucideReact[iconName];
  }
  
  // Depois tentar ícones essenciais
  if (ESSENTIAL_ICONS[iconName as keyof typeof ESSENTIAL_ICONS]) {
    return ESSENTIAL_ICONS[iconName as keyof typeof ESSENTIAL_ICONS];
  }
  
  // Retornar placeholder se nada for encontrado
  return ESSENTIAL_ICONS.Placeholder;
};

// Componente de ícone seguro
export const Icon: React.FC<{ 
  name: string; 
  className?: string; 
  size?: number;
  color?: string;
}> = ({ name, className = '', size = 24, color }) => {
  const IconComponent = getIcon(name);
  
  return (
    <span 
      className={`inline-flex items-center justify-center ${className}`}
      style={{ 
        width: size, 
        height: size,
        color: color,
        minWidth: size,
        minHeight: size
      }}
    >
      <IconComponent />
    </span>
  );
};

